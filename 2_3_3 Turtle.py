import turtle
import time

# - Programme principal -----------------------------------------------------------------------------------------------
t = turtle.Turtle()
t.reset()
variable = 0
while variable<12:
    variable = variable + 1
    t.forward(150)
    t.left(150)

time.sleep(2)
